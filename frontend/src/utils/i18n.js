const getLanguage = () => {
  const lang = navigator.language || navigator.userLanguage;
  return lang.startsWith('pt') ? 'pt' : 'en';
};

export const t = (key) => {
  const lang = getLanguage();
  return translations[lang][key] || translations['en'][key] || key;
};

export default t;
